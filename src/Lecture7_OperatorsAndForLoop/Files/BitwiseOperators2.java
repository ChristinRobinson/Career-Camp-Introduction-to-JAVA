package Lecture7_OperatorsAndForLoop.Files;

public class BitwiseOperators2 {
    public static void main(String args[]) {
        int a=10;
        int b=2;
        //int c=a&b
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(a<<3);
        System.out.println(a>>2);
    }
}
